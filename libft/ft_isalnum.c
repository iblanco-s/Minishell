#include "libft.h"

int	ft_isalnum(char a)
{
	if ('0' <= a && a <= '9')
		return (1);
	else if (('A' <= a && a <= 'Z') || ('a' <= a && a <= 'z'))
		return (1);
	else
		return (0);
}
